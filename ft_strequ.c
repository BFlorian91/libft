#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	if (s1 != NULL && s2 != NULL)
	{
		while (*s1 == *s2)
		{
			if (!*s1 && !*s2)
				return (1);
			++s1;
			++s2;
		}
	}
	return (0);
}
